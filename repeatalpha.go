package main

import "fmt"

func RepeatAlpha(s string) string {
	result := ""

	for i := 0; i < len(s); i++ {
		ch := s[i]
		count := 1
		if ch >= 'a' && ch <= 'z' {
			count = int((ch - 'a') + 1)
		}
		if ch >= 'A' && ch <= 'Z' {
			count = int((ch - 'A') + 1)
		}
		for j := 0; j < count; j++ {
			result += string(ch)
		}
	}
	return result
}

func main() {
	fmt.Println(RepeatAlpha("abc"))
	fmt.Println(RepeatAlpha("Choumi."))
	fmt.Println(RepeatAlpha(""))
	fmt.Println(RepeatAlpha("abacadaba 01!"))
}
